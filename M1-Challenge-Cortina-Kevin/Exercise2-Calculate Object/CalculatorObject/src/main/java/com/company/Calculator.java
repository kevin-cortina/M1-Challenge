package com.company;

public class Calculator {

//INT Methods
    public static int addition(int a, int b) {
        return a + b;
    }

    public static int division(int a, int b) {

        return a/b;
    }

    public static int subtraction(int a, int b) {
        return a-b;
    }

    public static int multiplication(int a, int b){
        return a*b;
    }

//DOUBLES Methods

    public static double dblAddition(double a, double b) {
        return a + b;
    }

    public static double dblDivision(double a, double b) {
        return a/b;
    }

    public static double dblSubtraction(double a, double b) {
        return a-b;
    }

    public static double dblMultiplication(double a, double b){
        return a*b;
    }


}
